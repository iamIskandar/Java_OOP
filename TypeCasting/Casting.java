public class Casting
{
	public static void main(String[] args) {

		double var=15.25;
		int var1=(int)var;
		int val=20;
		float val1=(float)val;

		System.out.println(var1);
		System.out.println(val1);

		String number="20";
		int x=Integer.parseInt(number);

		System.out.println(x);



	}
}